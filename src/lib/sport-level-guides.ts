export function getSportLevelGuideSports(preferredSports: Sport[]) {
  if (preferredSports.length === 0) {
    return [...SPORT_OPTIONS];
  }

  const selected = preferredSports.filter((sport) => SPORT_OPTIONS.includes(sport));
  const rest = SPORT_OPTIONS.filter((sport) => !selected.includes(sport));

  return [...selected, ...rest];
}

export function getSportLevelGuideTitle(sport: Sport) {
  return `Уровни · ${SPORT_LABELS[sport]}`;
}
